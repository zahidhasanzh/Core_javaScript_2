var date = new Date()
console.log(date); //2020-04-17T02:26:16.993Z

console.log(date.toDateString()); //Fri Apr 17 2020

console.log(date.toTimeString()); //08:29:29 GMT+0600 (Bangladesh Standard Time)

console.log(date.toLocaleTimeString()); // 8: 29: 29 AM

console.log(date.getFullYear()); //2020
console.log(date.getMonth()); //3
console.log(date.getDate()); //17

console.log(date.getHours()); //8
console.log(date.getMinutes()); // 33
console.log(date.getSeconds());//54














